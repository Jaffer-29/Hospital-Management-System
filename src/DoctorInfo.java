public class DoctorInfo{

    String name, department, designation,schedule,workdays,contact,room;

    DoctorInfo(String name, String department, String designation, String schedule,
               String workdays, String contact, String room){

        this.name=name;
        this.department=department;
        this.designation=designation;
        this.schedule=schedule;
        this.workdays=workdays;
        this.contact=contact;
        this.room=room;

    }
}