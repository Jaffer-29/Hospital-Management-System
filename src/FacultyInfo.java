public class FacultyInfo{

    String name,role, department,shiftHours,workdays,contact,location;

    FacultyInfo(String name,String role,  String department, String shiftHours,
                String workdays, String contact,String location){

        this.name=name;
        this.role=role;
        this.department=department;
        this.shiftHours=shiftHours;
        this.workdays=workdays;
        this.contact=contact;
        this.location=location;

    }
}